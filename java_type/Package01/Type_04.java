package Package01;

public class Type_04 {
	
	public static void main(String[] args) {
		
		// 참조타입 (call by reference)
		// Byte, Short, Integer, Long, Float, Double, Character, Boolean
		
		System.out.println("----------Byte-----------");
		// 1. Byte
		System.out.println("The Byte class wraps a value of primitive type byte in an object.");
		System.out.println("기본타입 : byte  ->  참조타입 : Byte");
		
		
		
		System.out.println(" ");
		System.out.println("----------Short-----------");
		// 2. Short
		System.out.println("The Short class wraps a value of primitive type short in an object.");
		System.out.println("기본타입 : short  ->  참조타입 : Short");
		
		
		
		System.out.println(" ");
		System.out.println("----------Integer-----------");
		// 3. Integer
		System.out.println("The Integer class wraps a value of the primitive type int in an object.");
		System.out.println("기본타입 : int  ->  참조타입 : Integer");
		
		
		
		System.out.println(" ");
		System.out.println("----------Long-----------");
		// 4. Long
		System.out.println("The Long class wraps a value of the primitive type long in an object.");
		System.out.println("기본타입 : long  ->  참조타입 : Long");
		
		
		
		System.out.println(" ");
		System.out.println("----------Float-----------");
		// 5. Float
		System.out.println("The Float class wraps a value of primitive type float in an object.");
		System.out.println("기본타입 : float  ->  참조타입 : Float");


		System.out.println(" ");
		System.out.println("----------Double-----------");
		// 6. Double
		System.out.println("The Double class wraps a value of the primitive type double in an object.");
		System.out.println("기본타입 : double  ->  참조타입 : Double");
		
		
		
		System.out.println(" ");
		System.out.println("----------Character-----------");
		// 7. Character
		System.out.println("The Character class wraps a value of the primitive type char in an object.");
		System.out.println("기본타입 : char  ->  참조타입 : Character");
		
		
		
		System.out.println(" ");
		System.out.println("----------Boolean-----------");
		// 8. Boolean
		System.out.println("The Boolean class wraps a value of the primitive type boolean in an object.");
		System.out.println("기본타입 : boolean  ->  참조타입 : Boolean");
	}

}
